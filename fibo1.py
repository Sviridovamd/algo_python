def fib(n):
    cur1 = 1
    cur2 = 1

    if n > 2:
        for i in range(2, n):
            tmp = cur2
            cur2 = cur1 + cur2
            cur1 = tmp
    return cur2


def main():
    n = int(input())
    print(fib(n))


if __name__ == "__main__":
    main()